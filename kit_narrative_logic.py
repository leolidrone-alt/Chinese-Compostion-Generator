# kit_narrative_logic.py
import random

class NarrativeLogicKit:
    """
    叙事逻辑选择器 - 加权随机版
    按文学统计规律加权随机选择叙事结构，AI拥有最终否决权。
    用户暗示的结构优先级最高。
    """
    def __init__(self):
        # 结构库：名称、指南、基础权重
        self.structures = {
            "三起三落": {
                "guideline": (
                    "【叙事结构建议】本次可采用“三起三落”的叙事结构。"
                    "- 特点：波浪式起伏，每次起落都有具体事件支撑。"
                    "- 适用：普通人的奋斗史、命运多舛的人物传记。"
                    "- 情感曲线：起→落→起→落→起→落→归。"
                    "- 参考：《活着》福贵、《骆驼祥子》祥子。"
                ),
                "base_weight": 0.48
            },
            "高开低走": {
                "guideline": (
                    "【叙事结构建议】本次可采用“高开低走”的叙事结构。"
                    "- 特点：从巅峰开始，不可逆地缓慢滑落，没有反弹。"
                    "- 适用：家族没落、天才陨落、时代变迁中的个体沉浮。"
                    "- 情感曲线：从高峰持续下滑。"
                    "- 参考：《红楼梦》贾府、《了不起的盖茨比》盖茨比。"
                ),
                "base_weight": 0.20
            },
            "低开高走": {
                "guideline": (
                    "【叙事结构建议】本次可采用“低开高走”的叙事结构。"
                    "- 特点：从底层出发，持续攀升。"
                    "- 适用：寒门崛起、逆袭人生、奋斗成功的故事。"
                    "- 情感曲线：从低谷持续上升。"
                    "- 参考：《简·爱》、《阿甘正传》。"
                ),
                "base_weight": 0.15
            },
            "环形结构": {
                "guideline": (
                    "【叙事结构建议】本次可采用“环形结构”。"
                    "- 特点：开头和结尾形成闭环，首尾呼应。"
                    "- 适用：哲思散文、回忆录、以某个意象贯穿全文的叙事。"
                    "- 情感曲线：从某起点出发，经历起伏后回归原点，但意境已变。"
                    "- 参考：《百年孤独》开篇、《老人与海》。"
                ),
                "base_weight": 0.10
            },
            "多线并行": {
                "guideline": (
                    "【叙事结构建议】本次可采用“多线并行”的叙事结构。"
                    "- 特点：多条叙事线索交替推进，最终交汇。"
                    "- 适用：家族史诗、多主角叙事、时代画卷式故事。"
                    "- 情感曲线：各条线索有自己的情感弧线，交汇处形成高潮。"
                    "- 参考：《战争与和平》、《红楼梦》（家族线+个人线）。"
                ),
                "base_weight": 0.07
            },
        }

    def get_structure_guideline(self) -> str:
        """
        按基础权重加权随机选择一个叙事结构，返回其指南文本。
        三起三落被选中的概率最高（48%），多线并行最低（7%）。
        """
        names = list(self.structures.keys())
        weights = [self.structures[name]["base_weight"] for name in names]
        chosen_name = random.choices(names, weights=weights, k=1)[0]
        return self.structures[chosen_name]["guideline"]